export function getIndexValue(type: IndexType, dateStr: string): number | null {
  const data = INDEX_DATA[type]
  if (!data) return null
  if (data[dateStr]) return data[dateStr]
  const [year, month] = dateStr.split('-').map(Number)
  for (let m = month; m >= 1; m--) {
    const key = `${year}-${String(m).padStart(2, '0')}`
    if (data[key]) return data[key]
  }
  for (let y = year - 1; y >= 2010; y--) {
    for (let m = 12; m >= 1; m--) {
      const key = `${y}-${String(m).padStart(2, '0')}`
      if (data[key]) return data[key]
    }
  }
  return null
}

export function getLatestIndex(type: IndexType): { date: string; value: number } | null {
  const data = INDEX_DATA[type]
  if (!data) return null
  const keys = Object.keys(data).sort()
  const lastKey = keys[keys.length - 1]
  return { date: lastKey, value: data[lastKey] }
}

export function getReferenceIndex(type: IndexType, judgmentDate: string): { date: string; value: number } | null {
  const [year, month] = judgmentDate.split('-').map(Number)
  const refMonth = month - 2
  const refYear = refMonth <= 0 ? year - 1 : year
  const refMonthAdj = refMonth <= 0 ? refMonth + 12 : refMonth
  const refDateStr = `${refYear}-${String(refMonthAdj).padStart(2, '0')}`
  const value = getIndexValue(type, refDateStr)
  if (!value) return null
  return { date: refDateStr, value }
}
